def parse_csp(settings):
    new_settings = []
    for s in settings:
        if s.lower() == "nonce":
            new_settings.append(CSP_NONCE)
        elif s.lower() in ["self", "'self'"]:
            new_settings.append(CSP_SELF)
        else:
            new_settings.append(s)
    return new_settings


